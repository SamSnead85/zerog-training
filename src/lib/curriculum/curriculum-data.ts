// Helper functions
export function getCurriculumByCategory(categoryId: string): CurriculumModule[] {
    return curriculumModules.filter((m) => m.category === categoryId);
}

export function getPopularCurriculum(): CurriculumModule[] {
    return curriculumModules.filter((m) => m.popular);
}

export function getTrendingCurriculum(): CurriculumModule[] {
    return curriculumModules.filter((m) => m.trending);
}

export function getNewCurriculum(): CurriculumModule[] {
    return curriculumModules.filter((m) => m.new);
}

export function searchCurriculum(query: string): CurriculumModule[] {
    const q = query.toLowerCase();
    return curriculumModules.filter(
        (m) =>
            m.title.toLowerCase().includes(q) ||
            m.description.toLowerCase().includes(q) ||
            m.tags.some((t) => t.toLowerCase().includes(q))
    );
}

export function getCurriculumById(id: string): CurriculumModule | undefined {
    return curriculumModules.find((m) => m.id === id);
}
